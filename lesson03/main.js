// Statements (инструкции), expressions (выражения), operators (операторы).

// операторы
// арифметические
console.log(2 + 3);
console.log(12 - 3);
console.log(2 * 3);
console.log(12 / 3);
console.log(11 % 3);
console.log(2 + 3 * 5);

let i = 4, d = 4;
console.log(++i);
console.log(--d);
let i2 = 4, d2 = 4;
console.log(i2++);
console.log(d2--);

// сравнения
let a = 4, b = 7, c = '4';
console.log(a < b);
console.log(a <= b);
console.log(a >= b);
console.log(a > b);
console.log(a == c);
console.log(a != c);
console.log(a === c);
console.log(a !== c);
